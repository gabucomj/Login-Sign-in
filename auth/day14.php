<h2>Tips for self-quarantine</h2>

*Prevent contact with family members and relatives.It is**
better to stay in a separate room and use a separate bathroom,
if available.

*Reports have indicated that pets can test positive for
COVID-19 but cannot transmit it to humans and WHO has also
said that no such transmission reports have been found . 

*It is therefore better to limit contact with the pets as
Coronavirus is a zoonotic virus and our pets should be safe.

*Prevent meeting visitors from outside unless very urgent 
or necessary. Do not go outside frequently for daily needs.

*For any medical attention, be prepared to tackle the situation
wisely and seek proper medical help from authorized nursing homes
and hospitals instead of involving your family members or friends.

*A face mask is always advised when around people, inside or 
outside the house.

*A tissue is always handy to cover the mouth and nose while 
sneezing. Make sure that the tissue is not re-used and your hands 
and face are washed properly with soap and sanitizers to maintain
a proper hygiene.

*Household items and personal items should not be shared and if in
case they are used by more than one person,it is important to wash 
and sanitize them thoroughly beforehand.

*Cleanliness and hygiene are the best prevention possible in times
of self-quarantine.Prevent any stain of body fluids or blood on
solid surfaces.

*Monitoring the symptoms every day is very important so that you 
can call your healthcare provider without any delay.





