When to self-isolate and what to do?</h2>
<h3>
What is self-isolation?</h3>
Self-isolation is when you stay at home because you have or might 
have coronavirus (COVID-19).This helps stop the virus spreading 
to other people.

Self-isolation is different to:

* Social Distancing – general advice for everyone to avoid close 
contact with other people

* Shielding – advice for people at high risk from coronavirus

<h4> When to self-isolate </h4>
*You have any symptoms of coronavirus (a high temperature, a new, 
continuous cough or a loss or change to your sense of smell or taste)

*You've tested positive for coronavirus – this means you have coronavirus

*You live with someone who has symptoms or has tested positive
someone in your support bubble has symptoms or has tested positive.

<h4> Mental Health Dos and Don'ts To Follow During The Lockdown </h4>
*Acknowledge Distress- allow yourself to acknowledge the distress that
you are feeling in this time of turmoil.

*Practice Mindfulness- focus on today and the present instead of the 
future and what tomorrow holds.

*Maintain Physical And Psychological hygiene- you can do so by keeping
your sleep cycle regular, grooming your face and body, eating nourishing
food and following a healthy diet.

*Practice Social Not Emotional Distancing- Continue to stay connected to
your friends, family and loved ones via digital means like video calls.

*Engage With Family- Use the time to partake in group activities with 
family members like indoor games, movie nights and cooking together on 
the weekends.

*Divide Responsibly- Practice fair division of household responsibilities
among family members so that everyone chips in.

*Stay Active- Keep sweating it out by building an indoor physical routine 
and engaging in hobbies you may not get to otherwise.

*Set Boundaries- Clearly demarcate working and non-working hours and draw 
the lines between them clearly.

*Avoid Panic- Nothing good comes from it. Breathe easy and stay in the present.

