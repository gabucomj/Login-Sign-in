being handpicked perhaps to experience it, understand it, have my own realizations 
that are very necessary in life, and give me more purpose, perhaps," she shares.
  
Iza was the 878th patient in the country to test 
positive for the novel coronavirus (COVID-19) disease.
 
Today, she joins the hundreds of Filipinos who have made full, often miraculous, 
recoveries.

 And like her fellow survivors, their loved ones, the healthcare workers 
who cared for them, and everyone who has become deeply involved in this crisis, 
she has much to teach us all about life.

"Being in the hospital and that whole COVID experience has made me realize 
what really matters in life. Relationships are number one: relationship with God, 
relationships with people around you... It’s such a humbling experience of really 
recognizing how people really need people, how we need each other in these times 
of darkness. How we need hope, how we need our faith—these are the things 
that matter most," she reflects.
