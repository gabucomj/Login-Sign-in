What is the difference between a PUI and PUM person?</h2>
The basic difference between the two is that PUIs require hospital admission whereas PUMs only need<br>to self-quarantine for 14 days.

"PUI"

A person is declared a PUI when he or she is exhibiting the coronavirus symptoms, has traveled in the<br>past 14 days to areas with issued travel restriction, and has a history of exposure to COVID-19. DOH<br>will consider symptoms such as high fever (≥38°C), cough, and other respiratory symptoms. History of<br>exposure include: A) contact with a confirmed or probable case of COVID-19 infection and B) worked<br>in or attended a health care facility where patients with confirmed or probable COVID-19 patients<br>were being treated.

"PUM"

A PUM, on the other hand, is not showing any coronavirus symptoms but has a travel history to areas<br>with issued travel restriction or a history of exposure to the novel virus.

