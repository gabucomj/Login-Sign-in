<h2>Spending the weekend at home isn’t ideal,
but this is a national emergency. </h2>

<h3>Clean and get organized</h3>
This reporter will not indulge in the misnomer that “cleaning can be fun,” 
however it is something that’s necessary to do.Do ALL of your laundry, 
purge your home clean, and tidy up like there’s no tomorrow.

<h3>Exercise</h3>
Quarantine or not there’s still plenty of ways to stay in good health.
It might not be the same as your routine workout but 
there’s plenty of options and workouts that are being streamed to do from home.

<h3>Explore ways to save money</h3>
With no going out, no commutation costs, and many more ways to save a little bit
of dough, now is the chance to go over your own finances to evaluate where you can 
save up during COVID-19 times

<h3>Check in on those close to you</h3>
Having strong mental health in times of seclusion is certainly as important as having 
good physical standing too. Call or Facetime your friends and family routinely, 
it will be much appreciated. 

<h3>Fun things to do</h3>
Video chat like you’re in middle school again. Many of us remember the days of group
video chats on weekday evenings back from our younger days. Who’s to say you can’t 
have a night like you’re back in the seventh grade procrastinating your homework 
with your buddies?

There’s nothing wrong with having a little fun to get through a challenging time.

<h3>Start a podcast</h3>
All you’ll need is a phone and free online editing software.

Start a quarantine confessional, go on a rant about the things that peeve you the most, 
or talk about the good in the world or how much we all miss sports! Keeping your mind 
sharp is important now.

<h3>Do karaoke</h3>
Fortunately, nobody will be watching which should add a layer of encouragement for many.

There’s tons of free websites where you can not only belt out some high notes, but also have
your friends join. Sing off the hard times.

<h3>Redecorate</h3>
Let your creative side show and take an opportunity to make some fresh and new executive
decisions for your living space. Move things around, play with your decor, give things a
springy new feeling!

