What should I do if I’ve been told to
self-quarantine or self-isolate?</h2>
While it may be scary being told to self-quarantine or self-isolate,
it’s important to keep in mind that the vast majority of people 
who have contracted COVID-19 thus far have experienced mild symptoms 
similar to the common cold or flu, and have recovered fully. 
That being said, the virus can be fatal for those who are considered high-risk.
That’s why it’s very important we take the necessary steps to prevent the virus from spreading. 
Here’s how you can protect yourself and others 
during your quarantine or isolation:

Stay home

Limit your out-of-home activity to medical care and do not go into school or work.
You should also avoid public transportation, ride-sharing, and taxis. If you need groceries,
try using a meal or grocery delivery service where your food
can be dropped off right outside your door.

Separate yourself

The CDC recommends keeping to one room as much as possible if you live with
roommates or family members. You should also use a separate bathroom if possible.

Wear a mask

If you are sick and must go in public or be around other people, 
it’s important to wear a face mask to prevent spreading the virus. 
Remember to also put a facemask on before entering the doctor’s office or when traveling 
to and from your doctor’s appointments. If you don’t have one upon arriving, your provider s
hould provide one for you that you can keep on-hand for transportation home and future use.

Stay away from pets

In the last month, two cats in New York became the first household pets in the U.S.
to test positive for COVID-19. This is believed to be the result of close contact with
infected humans. While there have not been any confirmed reports of pets transmitting 
COVID-19 to humans, it appears the virus can pass from people to animals. 
It is recommended that people sick with COVID-19 limit contact with animals 
until more information is known about the virus. If you must care for a pet while you are sick, 
make sure to wear a face mask and wash your hands before and after interacting with your pet.

Do not share personal items

To avoid spreading germs, do not share dishes, utensils, towels, or other personal 
items with other people in your household. After using these items, 
they should be washed thoroughly with soap and water.

Wash your hands

Wash your hands often with soap and water for at least 20 seconds, 
and if soap and water aren’t available, use an alcohol-based hand sanitizer.

Clean and disinfect surfaces frequently

Wipe down “high-touch” surfaces like countertops, doorknobs, toilets, 
phones, and keyboards, using household cleaners and wipes.

Exercise

Staying active will keep you both mentally and physically healthy 
during your quarantine. Exercise can help reduce elevated cortisol levels,
as well as trigger the release of endorphins, boosting your overall mood and happiness. 
If you are feeling well enough and don’t have active symptoms, 
try at an-home workout to avoid physical contact with others. 
Even walking up a few flights of stairs, 
or having a dance party in your room can be helpful.

Stay in contact with friends and family

Research shows that quality time with people you care about can boost your happiness level.
 If you are self-quarantined, try to still connect with your 
 friends and family through video chat or phone calls.

Monitor your symptoms

If you find your symptoms worsening, contact your primary 
care provider or reach out to our 24/7 virtual care team. Please avoid coming 
to a One Medical office or other healthcare facility unless directed to do so 
by your healthcare provider. If you are experiencing a medical emergency, 
please call 911.

Even if you are not experiencing COVID-19 symptoms, 
it’s important to follow these measures for the full quarantine or isolation period.
 If you are not sure whether to discontinue your isolation, please contact your healthcare provider.

